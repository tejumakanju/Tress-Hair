function makeVariants(
  baseSku: string,
  basePrice: number,
  lengths: string[],
  densities: string[],
  colors: string[],
  capSizes: string[] = ["Medium (22\")", "Large (23\")"]
): ProductVariant[] {
  const variants: ProductVariant[] = [];
  lengths.forEach((length) => {
    densities.forEach((density) => {
      colors.forEach((color) => {
        capSizes.forEach((capSize) => {
          const lengthMod = lengths.indexOf(length) * 15;
          const densityMod = densities.indexOf(density) * 20;
          variants.push({
            id: `${baseSku}-${length}-${density}-${color}-${capSize}`.replace(/\s/g, ""),
            length,
            density,
            color,
            capSize,
            price: basePrice + lengthMod + densityMod,
            sku: `${baseSku}-${length.slice(0, 2)}-${density.slice(0, 3)}-${color.slice(0, 3)}`.toUpperCase(),
            inStock: true,
          });
        });
      });
    });
  });
  return variants;
}

